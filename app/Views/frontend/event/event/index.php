                  <p>6/ Giá trị tương đương học bổng Chính phủ CSC (trong mùa dịch)</p>
                  <p>7/ 100% trúng tuyển, điều kiện đầu vào thấp hơn CSC</p>
                  <p>8/ Được cam kết đào tạo tiếng Trung tại Thanhmaihsk hoặc đối tác của Thanhmaihsk để thi đạt chứng chỉ HSK4 + HSKK trung cấp sau đó sẽ được vào thẳng năm 2 Đại học.</p>
